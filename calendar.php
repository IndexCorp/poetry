<h3><strong>The Palette Poetry Prize</strong></h3>
<p style="text-align: right;"><em>Closed August 15, 2021</em></p>
<p>Our biggest prize of the year, the Palette Poetry Prize is for all poets writing in English and will give out $4000 for a winning poems and publication on Palette Poetry<em>. </em>This year, the judge is Jericho Brown. Second and third place will receive $300 and $200, respectively.</p>
<hr />
<h3><strong>The Previously Published poems Prize</strong></h3>
<p style="text-align: right;">November - January 2022</p>
<p>Our landscape has exploded with presses and magazines over the last few decades with the advent of digital publications—so many good poems have been found, celebrated, and, unfortunately, forgotten. We want to bring light to those lost pieces of poetry—the work that you love but no longer receives the attention it deserves. Send us your favorite previously published poems for an opportunity to see it re-published and shared to our audience, along with a $2500 prize.</p>
<hr />
<h3><strong>The Emerging Poet Prize</strong></h3>
<p style="text-align: right;">February - April 2022</p>
<p>For emerging poets, this contest only accepts submissions from authors with fewer than two full-length collections out at the time of submission. The winning poet will be awarded $3000 and publication on Palette Poetry. Second and third place will win $300 &amp; $200 respectively, as well as publication. The top ten finalists will also be recognized, and the winning poems will be selected by our guest judge.</p>
<hr />
<h3><strong>The Sappho Prize</strong></h3>
<p style="text-align: right;">April - June 2022</p>
<p>We're honored to make space for women for our Sappho Prize. This prize will give out $2500 for a winning poems and publication on Palette Poetry<em>. </em>Second and third place will receive $300 and $200, respectively.</p>
