<h2></h2>
<h2 style="padding-left: 40px;">The 2021 Previously Published Poem Prize</h2>
<p style="text-align: right;"><em>Contest closed on January 17, 2021.</em></p>
<p>1st — <a href="../2021/05/25/the-distinguished-thing-a-colloquy/index.php">Diane Kerr, "The Distinguished Thing: A Colloquy"</a></p>
<p>2nd — <a href="../2021/05/26/if-im-caught-between-a-badge-and-a-hard-place-three-hours-after-the-street-lights-turn-on/index.php">Darius Simpson, “If I’m Caught Between a Badge and a Hard Place Three Hours After the Streetlights Turn On"</a></p>
<p>3rd — <a href="../2021/05/27/aftermath/index.php">Catherine-Esther Cowie, “Aftermath”</a></p>
<p>Our landscape has exploded with presses and magazines over the last few decades with the advent of digital publications—so many good poems have been found, celebrated, and, unfortunately, forgotten. We want to bring light to those lost pieces of poetry—the work that you love but no longer receives the attention it deserves. 1st Place: $2500 + Publication; 2nd Place: $300 + Publication; 3rd Place: $200 + Publication.</p>
<p><strong>Submission Guidelines</strong></p>
<ul>
<li>Submissions are open internationally, to any poet writing in English—other languages are okay to include, as long as the meat of the poem is in English.</li>
<li>There is no page requirement, but submission must be no more than 3 poems. Please submit all your poems in ONE document.</li>
<li>The poems must have been previously published online or in print to be eligible.</li>
<li>The poems must not have won any previous awards of $500 or more.</li>
<li>DO NOT INCLUDE the publication history of the individual poems in the document. Our readers will read these poems blind.</li>
<li>We do accept multiple submissions, but each submission will include the reading fee.</li>
</ul>
