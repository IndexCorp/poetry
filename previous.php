<p>We're honored to be able to create space for <strong>women*</strong> poets to step forward. Thanks to the efforts of the VIDA project and others, we've made a lot of progress in the past few years, but much work is still to be done. Palette is YOURS as much as it is ours, and we'd love for the magazine be useful in making our community a better, fuller, richer home.</p>
<p>This contest only accepts submissions from <strong>women* poets</strong>. The winning poet will be awarded <strong>$3000 and publication</strong> on Palette Poetry. Second and third place will win <strong>$300 &amp; $200</strong> respectively, as well as publication.</p>
<p>The top ten finalists will be selected by the editors, and guest judge <strong>Maggie Smith</strong> will then select the winner and two runner ups. Winners and finalists will be announced toward the end of August 2021.</p>
<p><strong>*all women are welcome to submit (cis, trans, etc)</strong></p>
