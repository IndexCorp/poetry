<h2></h2>
<h2 style="text-align: left;">The 2021 Emerging Poet Prize</h2>
</blockquote>
<p style="text-align: right;"><em>Contest closed on April 19, 2021.</em></p>
<p><span style="font-size: 16px;">1st — <a href="../2021/07/13/black-madonna/index.php">Crystal Valentine, "Black Madonna"</a></span></p>
<p><span style="font-size: 16px;">2nd — <a href="../2021/07/14/slipped-stitches/index.php">Christine Poreba, "Slipped Stitches"</a></span></p>
<p><span style="font-family: system-ui, '---apple-system', BlinkMacSystemFont, 'Segoe UI', Roboto, Oxygen, Ubuntu, Cantarell, 'Open Sans', 'Helvetica Neue', sans-serif; font-size: 16px;">3rd — <a href="../2021/07/15/the-present-of-poetry/index.php">Serrina Zou, "The Present of Poetry"</a></span></p>
<p>For emerging poets, this contest only accepts submissions from authors with fewer than two full-length collections out at the time of submission. The winning poet will be<strong> awarded $3000</strong> and publication on Palette Poetry. Second and third place will win $300 &amp; $200 respectively, as well as publication.</p>
<p>The top ten finalists will be selected by the editors, and guest judge<strong> Kelli Russell Agodon</strong> will then select the winner and two runner-ups.</p>
<p><a href="https://www.agodon.com/index.php"><span style="font-size: medium;">Kelli Russell Agodon</span></a> is the author <em><span style="font-family: system-ui, '---apple-system', BlinkMacSystemFont, 'Segoe UI', Roboto, Oxygen, Ubuntu, Cantarell, 'Open Sans', 'Helvetica Neue', sans-serif;"><a href="https://bookshop.org/books/dialogues-with-rising-tides/9781556596155">Dialogues with Rising Tides</a>, </span></em><span style="font-family: system-ui, '---apple-system', BlinkMacSystemFont, 'Segoe UI', Roboto, Oxygen, Ubuntu, Cantarell, 'Open Sans', 'Helvetica Neue', sans-serif;">now available from Copper Canyon Press. </span>She is also the author of <em>Hourglass Museum, Letters from the Emily Dickinson Room</em>, <em>Small Knots, </em>and <em>Geography</em>.  She coedited the anthology <em>Fire On Her Tongue</em> and wrote <em>The Daily Poet: Day-By-Day Prompts For Your Writing Practice </em>with Martha Silano. She is cofounder of Two Sylvias Press. www.agodon.com / www.twosylviaspress.com</p>
<p><strong>Submission Guidelines</strong></p>
<ul>
<li>For this Prize, we are only accepting work from emerging poets—poets with no more than two full-length collections published at the time of submission.</li>
<li>Submissions are open internationally, to any poet writing in English—other languages are okay to include, as long as the meat of the poem is in English.</li>
<li>We accept simultaneous submissions—just please send us a note if your work is picked up elsewhere. (We want to say congrats!)</li>
<li>There is no page requirement, but submission must be no more than 3 poems. Please submit all your poems in ONE document.</li>
<li>We are only accepting unpublished work with this contest.</li>
<li>We do accept multiple submissions, but each submission will include the reading fee.</li>
<li>Please include a brief cover letter with your publication history.</li>
<li>Review our FAQ page for frequently asked questions.</li>
<li>Contest closes on Sunday, April 19th, 2021.</li>
<li>Reading fee is $20.</li>
</ul>
