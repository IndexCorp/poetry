<h2 style="text-align: left;">The 2021 Palette Poetry Prize</h2>
<p style="text-align: right;"><em><span style="color: #000000;">Contest closed on August 16, 2021.</span></em></p>
<p>1st — Reading in progress. TBA in November 2021.</p>
<p>2nd — Reading in progress. TBA in November 2021.</p>
<p>3rd — Reading in progress. TBA in November 2021.</p>
<p><span style="color: #000000;">Thank you all for the time, energy, and heart you put into your poems for this contest. We're grateful for the opportunity to read your work, and are aiming to announce finalists and winners in late October 2021. Palette's editors will select the ten finalists. Second and third place will receive $300 and $200, respectively. The winner will be selected by our guest judge, <strong>Jericho Brown</strong>.</span></p>
<h3><span style="color: #000000;"><strong>SUBMISSION GUIDELINES</strong></span></h3>
<ul>
<li><span style="color: #000000;">Submissions are open internationally, to any poet writing in English—other languages are okay to include, as long as the meat of the poem is in English.</span></li>
<li><span style="color: #000000;">We accept simultaneous submissions—just please send us a note if your work is picked up elsewhere (We want to say congrats!).</span></li>
<li><span style="color: #000000;">There is no page requirement, but submission must be no more than 3 poems. Please submit all your poems in ONE document.</span></li>
<li><span style="color: #000000;">We do accept multiple submissions, but each submission will include the reading fee.</span></li>
<li><span style="color: #000000;">Please include a brief cover letter with your publication history.</span></li>
<li><span style="color: #000000;">Review <a style="color: #000000;" href="../faq.html">our FAQ page for frequently asked questions.</a></span></li>
<li><span style="color: #000000;">Includes a $20 Reading fee.</span></li>
</ul>
