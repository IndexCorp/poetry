<h2 style="text-align: center;"><span style="font-size: 26px;"><strong>Our Values</strong></span></h2>
<p>We at Palette believe in creating a space for free, safe, life-affirming expression — to this end, we do not discriminate on the basis of age, religion, ability, socioeconomic status, gender identity or expression, national origin, ancestry, race, sex or sexual orientation, or any other reason.</p>
<p>It would be impossible for us to curate a space that we really considered free and life-affirming, however, if we only extended this promise of safety from discrimination to our submitters and not our audience as well. That means that just as we won’t discriminate against our submitters on the basis of their identity, we do not tolerate discrimination in the poetry we consider for publication.</p>
<p>Any work submitted to us found to be discriminatory will be declined and returned unread.</p>
<p>Following VIDA/CLMP #saferLIT’s initiative, we offer our platform as a safe space for survivors — we commit “To believe victims who come forward about their experiences and to do what [we] can to assist in getting help and address[ing] the situation immediately. To not deny the experience, gaslight, or dismiss those who come forward.” We commit to removing from our platform any writers we’ve published if we learn of their bigotry or harassment elsewhere, past, present, or future. If you need to reach our editor, please send an email to sarah (at) palettepoetry.com.</p>
<p>We know well that the publishing world has been and is a feature of systemic racism and discriminatory practices. Palette is proud to stand with our community in leading this industry in the right direction, toward anti-racism, toward equity of opportunity and expression, and toward justice.</p>
<p>Inequality observed is not inequality dismantled — mission statements such as this are a step toward, and not the arrival of, true inclusion. Our community today owes so much to historically marginalized writers, whose contributions to poetry and all other art forms are cherished just as quickly as their authors are under-appreciated, overlooked, and further marginalized. It is our job — and the job of the entire publishing industry — to work to change this daily.</p>
<p>This statement represents who we at Palette believe we are, and what we believe ourselves to be. It requires us as well to acknowledge there is a rift between who we imagine ourselves to be and who we are. We hope that our community will continue to hold us accountable to our mission statement and our mistakes.</p>
<p>Happy reading, happy writing, and may you feel safe, free, and have your life affirmed always,</p>
<p>The Palette Team</p>
