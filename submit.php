<h2>Featured Poetry</h2>
<h3>always free. always open.</h3>
<p>Submissions for our Featured Poetry category are open year-round to poets at any stage of their careers. Featured poems are published online only and will spotlight a number of poems from new authors each month. We highly encourage emerging authors to submit.</p>
<p>We are thrilled to offer significant payment to our poets: $50 per poems, up to $150. We are proud to be able to pay our artists, but will be highly selective in our choices for publication.</p>
<p>We also warmly invite under-represented and marginalized voices to submit. Our aim is to be an accurate representation of the wonderful diversity of our community. Your voice is valued here, so to help do our part in moving forward the needle on equity in publishing, we've opened a special category for historically marginalized voices to send their work directly to our editorial team and receive a response within two weeks. <a href="https://palettepoetry.submittable.com/submit/169282/quick-response-for-historically-marginalized-authors">More info here.</a></p>
