<p>We’re delighted to offer the inaugural Love &amp; Eros Prize for 2021: $3000 and publication! Love engulfs us, desire rules us. We encourage love poems beyond the confines of what’s traditionally “romantic.” Rather, we’re seeking unflinching examinations of our sharpest human sensations—those of desire, longing, devotion, and intimacy. All iterations of love are welcome—send us your bitter and your sweet, your queer, platonic, reverent, and devotional.</p>

<p><span data-offset-key="1o428-6-0">Palette's editors will choose the ten finalists and any honorable mentions that warrant extra attention. Our judge will then select the winner and runner-ups. </span><span data-offset-key="1o428-6-1">Second and third place will receive $300 and $200, respectively.</span></p>

<p>The winner will be selected by our guest judge, Kaveh Akbar, author of <em>Pilgrim Bell </em><span data-offset-key="ekjr5-0-2">(Graywolf, 2021). </span></p>

<h4 data-offset-key="ekjr5-0-0"><strong>about our judge</strong></h4>

<div class="" data-block="true" data-editor="49dco" data-offset-key="a2q8t-0-0">
<p class="public-DraftStyleDefault-block public-DraftStyleDefault-ltr" data-offset-key="a2q8t-0-0"><span data-offset-key="a2q8t-0-1">Kaveh Akbar's</span><span data-offset-key="a2q8t-0-2"> poems appear in </span><em>The New Yorker, The New York Times, Paris Review, Best American Poetry</em><span data-offset-key="a2q8t-0-4"><em>,</em> and elsewhere. His second full-length volume of poetry, </span><em><a class="linkStyles_accessibleLink__2Rde7 sbm-text link" href="https://www.graywolfpress.org/books/pilgrim-bell" target="_blank" rel="noopener noreferrer" aria-label="">Pilgrim Bell</a></em><span data-offset-key="a2q8t-2-0">, was recently published by Graywolf in August 2021. His debut, </span><em><a class="linkStyles_accessibleLink__2Rde7 sbm-text link" href="http://alicejamesbooks.org/ajb-titles/calling-a-wolf-a-wolf/" target="_blank" rel="noopener noreferrer" aria-label="">Calling a Wolf a Wolf</a></em><span data-offset-key="a2q8t-4-0"><em>,</em> is out with Alice James in the US and Penguin in the UK. He is also the author of the chapbook, </span><em><a class="linkStyles_accessibleLink__2Rde7 sbm-text link" href="http://siblingrivalrypress.bigcartel.com/product/portrait-of-the-alcoholic-by-kaveh-akbar" target="_blank" rel="noopener noreferrer" aria-label="">Portrait of the Alcoholic</a></em><span data-offset-key="a2q8t-6-0">, published in 2016 by Sibling Rivalry Press. In 2022, Penguin Classics will publish a new anthology edited by Kaveh: </span><span data-offset-key="a2q8t-6-1"><em>The Penguin Book of Spiritual Verse: 100 Poets on the Divine</em>.</span></p>
